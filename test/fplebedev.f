      SUBROUTINE FPSPHERE
C   ********************************************************************
C   *                                                                  *
C   *  generate an angular mesh and spherical harmonics at those       *
C   *  mesh points. For an angular integration the weights are ge-     *
C   *  rated .                                                         *
C   *                                                                  *
C   *  originally by R. Zeller      Feb. 1996                          *
C   *                                                                  *
C   ********************************************************************
      USE MOD_CONSTANTS,ONLY:PI
      USE MOD_TYPES,ONLY:NLMFPMAX,NLFPMAX
      USE MOD_RMESH,ONLY:NMAX_LEBGRID,RHAT_LEBGRID,N_LEBGRID,W_LEBGRID,
     &    Y_LEBGRID
      IMPLICIT NONE
C*--FPSPHERE17
C
C*** Start of declarations rewritten by SPAG
C
C PARAMETER definitions
C
      CHARACTER*40 ROUTINE
      PARAMETER (ROUTINE='FPSPHERE')
C
C Local variables
C
      REAL*8 DNRM2
      REAL*8 F0,ND(3,3),R1,R2,R3,RABS,RHAT(3),RVEC(3),RYLM(:),W,W4PI
      INTEGER IA_ERR,I_LEBGRID,LM
C
C*** End of declarations rewritten by SPAG
C
      ALLOCATABLE RYLM
C
      ALLOCATE (RYLM(NLMFPMAX))
C
C-----------------------------------------------------------------------
      IF ( ALLOCATED(W_LEBGRID) ) THEN
         DEALLOCATE (RHAT_LEBGRID)
         DEALLOCATE (W_LEBGRID,Y_LEBGRID)
      END IF
C
      N_LEBGRID = NMAX_LEBGRID
C
C-----------------------------------------------------------------------
      ALLOCATE (RHAT_LEBGRID(3,NMAX_LEBGRID))
      ALLOCATE (W_LEBGRID(NMAX_LEBGRID,NLMFPMAX))
      ALLOCATE (Y_LEBGRID(NMAX_LEBGRID,NLMFPMAX),STAT=IA_ERR)
      IF ( IA_ERR.NE.0 )
     &     CALL STOP_MESSAGE(ROUTINE,'ALLOC: RHAT_LEBGRID')
C-----------------------------------------------------------------------
C
      DO I_LEBGRID = 1,N_LEBGRID
C
         CALL FPLEBEDEV(I_LEBGRID,R1,R2,R3,W,N_LEBGRID)
C
C      make a small rotation
C
         F0 = 0.08D0
C
         ND(1,1) = COS(F0)
         ND(1,2) = 0
         ND(1,3) = SIN(F0)
         ND(2,1) = 0
         ND(2,2) = 1
         ND(2,3) = 0.0D0
         ND(3,1) = -SIN(F0)
         ND(3,2) = 0
         ND(3,3) = COS(F0)
C
         RVEC(1) = ND(1,1)*R1 + ND(2,1)*R2 + ND(3,1)*R3
         RVEC(2) = ND(1,2)*R1 + ND(2,2)*R2 + ND(3,2)*R3
         RVEC(3) = ND(1,3)*R1 + ND(2,3)*R2 + ND(3,3)*R3
C
         RABS = DNRM2(3,RVEC,1)
         RHAT(1:3) = RVEC(1:3)/RABS
C
         RHAT_LEBGRID(1:3,I_LEBGRID) = RHAT(1:3)
C
         CALL CALC_RHPLM(RHAT(1),RHAT(2),RHAT(3),RYLM,NLFPMAX-1,
     &                   NLMFPMAX)
C
C----------- store and multiply the spherical harmonics with the weights
C
         W4PI = W*PI*4.D0
C
         DO LM = 1,NLMFPMAX
            Y_LEBGRID(I_LEBGRID,LM) = RYLM(LM)
            W_LEBGRID(I_LEBGRID,LM) = RYLM(LM)*W4PI
         END DO
C
      END DO
C
      WRITE (6,99001) NLFPMAX - 1,N_LEBGRID
      RETURN
C-----------------------------------------------------------------------
99001 FORMAT (/,1X,79('*'),/,35X,'<FPSPHERE>',/,1X,79('*'),//,10X,
     &        'weighted spherical harmonics created for l_max =',I3,/,
     &        10X,'on a Lebedev integration mesh with',I5,
     &        '  points on a sphere',/)
      END
